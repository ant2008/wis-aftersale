package com.wenc.commdomain.vo.dsb;

public class DistribSpreadSupplyVO {

    private String supplyId;

    private String supplyName;

    private String supplyAddress;

    private String supplyPhoneNo;

    private String supplyStarLevel;


    public String getSupplyId() {
        return supplyId;
    }

    public void setSupplyId(String supplyId) {
        this.supplyId = supplyId;
    }

    public String getSupplyName() {
        return supplyName;
    }

    public void setSupplyName(String supplyName) {
        this.supplyName = supplyName;
    }

    public String getSupplyAddress() {
        return supplyAddress;
    }

    public void setSupplyAddress(String supplyAddress) {
        this.supplyAddress = supplyAddress;
    }

    public String getSupplyPhoneNo() {
        return supplyPhoneNo;
    }

    public void setSupplyPhoneNo(String supplyPhoneNo) {
        this.supplyPhoneNo = supplyPhoneNo;
    }

    public String getSupplyStarLevel() {
        return supplyStarLevel;
    }

    public void setSupplyStarLevel(String supplyStarLevel) {
        this.supplyStarLevel = supplyStarLevel;
    }
}
